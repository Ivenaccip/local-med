INPUT_NAMES = {i.name for i in session.get_inputs()}
NEEDS_TOKEN_TYPE = "token_type_ids" in INPUT_NAMES

def mean_pooling(token_embeddings: np.ndarray, attention_mask: np.ndarray) -> np.ndarray:
    """Promedia embeddings de tokens ponderados por la attention mask."""
    mask = np.expand_dims(attention_mask, -1).astype(np.float32)
    summed = np.sum(token_embeddings * mask, axis=1)
    counts = np.clip(mask.sum(axis=1), a_min=1e-9, a_max=None)
    return summed / counts


def l2_normalize(vec: np.ndarray) -> np.ndarray:
    norm = np.linalg.norm(vec, axis=-1, keepdims=True)
    return vec / np.clip(norm, a_min=1e-9, a_max=None)


