def embed(text: str) -> list:
    """Embebe una sola query. Útil para el golden test."""
    enc = tokenizer(
        text, padding=True, truncation=True, max_length=128, return_tensors="np"
    )
    inputs = {
        "input_ids": enc["input_ids"].astype(np.int64),
        "attention_mask": enc["attention_mask"].astype(np.int64),
    }
    if NEEDS_TOKEN_TYPE:
        inputs["token_type_ids"] = enc.get(
            "token_type_ids", np.zeros_like(enc["input_ids"])
        ).astype(np.int64)

    outputs = session.run(None, inputs)
    token_embeddings = outputs[0]  # (1, seq_len, 384)
    pooled = mean_pooling(token_embeddings, enc["attention_mask"])
    normalized = l2_normalize(pooled)
    return normalized[0].tolist()

def embed_batch(texts: list, batch_size: int = 32) -> list:
    """Embebe muchas queries en batch. Mucho más rápido que one-by-one."""
    all_vecs = []
    for i in tqdm(range(0, len(texts), batch_size), desc="Embedding"):
        chunk = texts[i:i+batch_size]
        enc = tokenizer(
            chunk, padding=True, truncation=True, max_length=128, return_tensors="np"
        )
        inputs = {
            "input_ids": enc["input_ids"].astype(np.int64),
            "attention_mask": enc["attention_mask"].astype(np.int64),
        }
        if NEEDS_TOKEN_TYPE:
            inputs["token_type_ids"] = enc.get(
                "token_type_ids", np.zeros_like(enc["input_ids"])
            ).astype(np.int64)

        outputs = session.run(None, inputs)
        pooled = mean_pooling(outputs[0], enc["attention_mask"])
        normalized = l2_normalize(pooled)
        all_vecs.extend(normalized.tolist())
    return all_vecs

